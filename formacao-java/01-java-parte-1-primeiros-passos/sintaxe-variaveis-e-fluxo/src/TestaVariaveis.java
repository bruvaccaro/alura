public class TestaVariaveis {
	public static void main(String[] args) {
		int idade = 27;

		System.out.println("Eu tenho " + idade + " anos.");

		System.out.println("Daqui 3 anos eu terei " + (idade + 3) + " anos.");

		System.out.println("Eu nasci em " + (2020 - idade) + ".");

		System.out.println("A minha mãe tem o dobro da minha idade, que é " + (idade * 2) + " anos.");

		System.out.println("A minha irmã tem a metade da minha idade, que é " + (idade / 2) + " anos.");
	}
}